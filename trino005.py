    if opcao == "1":
        nome = input("nome:")
        telefone = input("telefone: ")
        agenda[nome] = telefone
    elif opcao == "2":
        nome = input("nome para remover:")
        if nome in agenda:
            del agenda[nome]
            print("removido.")
